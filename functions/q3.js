// Create a function called reverseArray that takes an array as input and returns a new array with the elements in 
// reverse order. For example, if the input is [1, 2, 3, 4], the function should return [4, 3, 2, 1].

function reverseArray(arr){
    return arr.reverse(); 
}
console.log(reverseArray([1,2,3,4]));